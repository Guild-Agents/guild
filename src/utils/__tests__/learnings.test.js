// --- estimateTokens ---

describe('estimateTokens', () => {
  it('returns 0 for empty string', () => {
    expect(estimateTokens('')).toBe(0);
  });

  it('returns 0 for null', () => {
    expect(estimateTokens(null)).toBe(0);
  });

  it('returns 0 for undefined', () => {
    expect(estimateTokens(undefined)).toBe(0);
  });

  it('returns 0 for whitespace-only', () => {
    expect(estimateTokens('   \n\t  ')).toBe(0);
  });

  it('returns positive number for a single word', () => {
    expect(estimateTokens('hello')).toBeGreaterThan(0);
  });

  it('returns a reasonable estimate for known text', () => {
    // 10 words × 1.35 = 13.5 → ceil → 14
    const text = 'one two three four five six seven eight nine ten';
    expect(estimateTokens(text)).toBe(14);
  });

  it('scales proportionally with text length', () => {
    const short = 'a b c d e';
    const long = 'a b c d e f g h i j k l m n o p q r s t';
    expect(estimateTokens(long)).toBeGreaterThan(estimateTokens(short));
  });
});

// --- isWithinBudget ---

describe('isWithinBudget', () => {
  it('returns true for short text', () => {
    expect(isWithinBudget('Hello world')).toBe(true);
  });

  it('returns false for very long text', () => {
    const longText = Array(2000).fill('word').join(' ');
    expect(isWithinBudget(longText)).toBe(false);
  });

  it('respects custom budget parameter', () => {
    const text = 'one two three four five'; // 5 words × 1.35 = 7 tokens
    expect(isWithinBudget(text, 10)).toBe(true);
    expect(isWithinBudget(text, 3)).toBe(false);
  });

  it('returns true for empty/null text', () => {
    expect(isWithinBudget('')).toBe(true);
    expect(isWithinBudget(null)).toBe(true);
  });
});

