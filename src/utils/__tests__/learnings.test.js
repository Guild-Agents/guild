// --- parseLearnings ---

const SAMPLE_LEARNINGS = `# Guild Learnings — test-project

> Auto-generated by Guild. Last updated: 2026-02-25T10:00:00.000Z
> Total executions: 3
> Token budget: this file should stay under 2000 tokens

## Project Patterns

- Error handling uses AppException hierarchy with error codes (discovered: 2026-02-24)
- Naming: services use PascalCase (discovered: 2026-02-25)

## Architecture Decisions

- Auth uses JWT with refresh tokens (discovered: 2026-02-23)

## Past Issues & Resolutions

_No learnings yet._

## User Preferences

- Prefers early returns over nested if/else (discovered: 2026-02-24)

## Agent Notes

- code-reviewer: auth changes need extra scrutiny (discovered: 2026-02-25)
`;

describe('parseLearnings', () => {
  it('parses header fields', () => {
    const result = parseLearnings(SAMPLE_LEARNINGS);
    expect(result.header.project).toBe('test-project');
    expect(result.header.lastUpdated).toBe('2026-02-25T10:00:00.000Z');
    expect(result.header.totalExecutions).toBe(3);
  });

  it('parses entries per section', () => {
    const result = parseLearnings(SAMPLE_LEARNINGS);
    expect(result.sections['Project Patterns']).toHaveLength(2);
    expect(result.sections['Architecture Decisions']).toHaveLength(1);
    expect(result.sections['Past Issues & Resolutions']).toHaveLength(0);
    expect(result.sections['User Preferences']).toHaveLength(1);
    expect(result.sections['Agent Notes']).toHaveLength(1);
  });

  it('extracts discovered dates from entries', () => {
    const result = parseLearnings(SAMPLE_LEARNINGS);
    expect(result.sections['Project Patterns'][0].discovered).toBe('2026-02-24');
    expect(result.sections['Architecture Decisions'][0].discovered).toBe('2026-02-23');
  });

  it('returns empty structure for empty string', () => {
    const result = parseLearnings('');
    expect(result.header.project).toBe('');
    expect(result.header.totalExecutions).toBe(0);
    for (const section of LEARNINGS_SECTIONS) {
      expect(result.sections[section]).toEqual([]);
    }
  });

  it('returns empty structure for null', () => {
    const result = parseLearnings(null);
    for (const section of LEARNINGS_SECTIONS) {
      expect(result.sections[section]).toEqual([]);
    }
  });

  it('handles content with no entries in any section', () => {
    const empty = renderEmptyLearnings('empty-project');
    const result = parseLearnings(empty);
    expect(result.header.project).toBe('empty-project');
    for (const section of LEARNINGS_SECTIONS) {
      expect(result.sections[section]).toEqual([]);
    }
  });

  it('ignores unknown sections', () => {
    const content = '# Guild Learnings — test\n\n## Unknown Section\n\n- entry one\n';
    const result = parseLearnings(content);
    expect(result.sections['Unknown Section']).toBeUndefined();
  });
});

// --- renderLearnings ---

describe('renderLearnings', () => {
  it('renders a parsed structure back to markdown', () => {
    const parsed = parseLearnings(SAMPLE_LEARNINGS);
    const rendered = renderLearnings(parsed);
    expect(rendered).toContain('# Guild Learnings — test-project');
    expect(rendered).toContain('## Project Patterns');
    expect(rendered).toContain('- Error handling uses AppException');
    expect(rendered).toContain('- Auth uses JWT');
  });

  it('preserves section entry counts in round-trip', () => {
    const parsed = parseLearnings(SAMPLE_LEARNINGS);
    const rendered = renderLearnings(parsed);
    const reparsed = parseLearnings(rendered);

    for (const section of LEARNINGS_SECTIONS) {
      expect(reparsed.sections[section].length).toBe(parsed.sections[section].length);
    }
  });

  it('renders empty sections with placeholder', () => {
    const parsed = parseLearnings(SAMPLE_LEARNINGS);
    const rendered = renderLearnings(parsed);
    // Past Issues has no entries — should have placeholder
    expect(rendered).toContain('_No learnings yet._');
  });

  it('renders empty learnings correctly', () => {
    const emptyParsed = parseLearnings('');
    const rendered = renderLearnings(emptyParsed);
    for (const section of LEARNINGS_SECTIONS) {
      expect(rendered).toContain(`## ${section}`);
    }
    const placeholders = rendered.match(/_No learnings yet\._/g);
    expect(placeholders).toHaveLength(5);
  });
});

